    /**
     * 根据imsi号获取国家
     * 
     * @param idCard
     *            身份编号
     * @return 年龄
     */
    public static String getCounty(String imsi) {
        if (imsi.length() != 15) {
            return null;
        }
        String country = imsi.substring(0, 3);
        
        return countryCodes.get(country);
    }
    
    /**
     * 根据imsi号获取运营商
     * 
     * @param idCard
     *            身份编号
     * @return 年龄
     */
    public static String getCarrier(String imsi) {
        if (imsi.length() != 15) {
            return null;
        }
        String carrier = imsi.substring(0, 5);
        
        return carrierCodes.get(carrier);
    }
    
    /**
	 * 校验imsi号码
	 * @param imsi
	 * @return
	 */
	public static boolean validate(String imsi){
		if (15 != imsi.length() && 14 != imsi.length()) {
			return false;
		}
		
		// imsi号是否属于某个国家
		if(null != getCounty(imsi)){
			return true;
		} else {
			return false;
		}
	}
	
