package exercise.com.leo.base.lang.inherit2string;

public class Animal {
	private String name;
	private String age;

	public String getName() {
		return name;
	}

	public Animal setName(String name) {
		this.name = name;
		return this;
	}

	public String getAge() {
		return age;
	}

	public Animal setAge(String age) {
		this.age = age;
		return this;
	}

}
